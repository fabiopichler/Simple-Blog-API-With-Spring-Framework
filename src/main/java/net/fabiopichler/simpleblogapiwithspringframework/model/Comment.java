package net.fabiopichler.simpleblogapiwithspringframework.model;

import jakarta.persistence.*;
import jakarta.validation.constraints.NotBlank;
import net.fabiopichler.simpleblogapiwithspringframework.util.DateTimeUtil;

import java.time.LocalDateTime;

@Entity
@Table(name = "comments")
public class Comment {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @ManyToOne
    @JoinColumn(name = "post_id")
    private Post post;

    @ManyToOne
    @JoinColumn(name = "user_id")
    private User user;

    @Column(name = "author_name", nullable = false)
    @NotBlank
    private String authorName;

    @Column(name = "author_email", nullable = false)
    @NotBlank
    private String authorEmail;

    @Lob
    @Column(columnDefinition = "TEXT")
    @NotBlank
    private String body;

    @Column(name = "created_at", nullable = false)
    private LocalDateTime createdAt = LocalDateTime.now();

    @Column(name = "updated_at", nullable = false)
    private LocalDateTime updatedAt = LocalDateTime.now();

    public Comment() {
    }

    public Comment(Long id, Post post, User user, String authorName, String authorEmail, String body, LocalDateTime createdAt, LocalDateTime updatedAt) {
        this.id = id;
        this.post = post;
        this.user = user;
        this.authorName = authorName;
        this.authorEmail = authorEmail;
        this.body = body;
        this.createdAt = createdAt;
        this.updatedAt = updatedAt;
    }

    public String getCreatedAtFormatted() {
        return createdAt.format(DateTimeUtil.getFormatter());
    }

    public String getUpdatedAtFormatted() {
        return updatedAt.format(DateTimeUtil.getFormatter());
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Post getPost() {
        return post;
    }

    public void setPost(Post post) {
        this.post = post;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public String getAuthorName() {
        return authorName;
    }

    public void setAuthorName(String authorName) {
        this.authorName = authorName;
    }

    public String getAuthorEmail() {
        return authorEmail;
    }

    public void setAuthorEmail(String authorEmail) {
        this.authorEmail = authorEmail;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }

    public LocalDateTime getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(LocalDateTime createdAt) {
        this.createdAt = createdAt;
    }

    public LocalDateTime getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(LocalDateTime updatedAt) {
        this.updatedAt = updatedAt;
    }
}
